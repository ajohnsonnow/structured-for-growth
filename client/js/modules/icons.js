/**
 * Generate an SVG icon string.
 *
 * @param {string} name - Icon name from the icons map
 * @param {Object} [options]
 * @param {number} [options.size=24] - Width and height in pixels
 * @param {string} [options.class=''] - Additional CSS class(es)
 * @param {string} [options.strokeWidth='2'] - SVG stroke width
 * @param {string} [options.ariaLabel] - Accessible label. If omitted, icon is decorative (aria-hidden).
 * @param {string} [options.color] - Stroke/fill color override
 * @returns {string} Complete SVG element as HTML string
 */
export function icon(name, options = {}) {
  const { size = 24, class: className = '', strokeWidth = '2', ariaLabel, color } = options;

  const pathData = icons[name];
  if (!pathData) {
    console.warn(`[icons] Unknown icon: "${name}"`);
    return '';
  }

  const ariaAttrs = ariaLabel
    ? `role="img" aria-label="${ariaLabel}"`
    : 'aria-hidden="true" focusable="false"';

  const colorAttr = color ? ` stroke="${color}"` : '';

  return `<svg class="icon icon-${name}${className ? ' ' + className : ''}" width="${size}" height="${size}" viewBox="0 0 24 24" fill="none" stroke="currentColor" stroke-width="${strokeWidth}" stroke-linecap="round" stroke-linejoin="round"${colorAttr} ${ariaAttrs}>${pathData}</svg>`;
}

/**
 * Get raw SVG string (same as icon but with defaults).
 * @param {string} name
 * @returns {string}
 */
export function getIconSvg(name) {
  return icon(name);
}

/**
 * Get list of all available icon names.
 * @returns {string[]}
 */
export function getIconNames() {
  return Object.keys(icons);
}

/**
 * Initialize icon system â€” find all elements with data-icon attribute
 * and replace them with the corresponding SVG.
 *
 * Usage in HTML:
 *   <span data-icon="dashboard" data-icon-size="20" data-icon-label="Dashboard"></span>
 */
export function initIcons() {
  const elements = document.querySelectorAll('[data-icon]');
  elements.forEach((el) => {
    const name = el.getAttribute('data-icon');
    const size = parseInt(el.getAttribute('data-icon-size') || '24', 10);
    const label = el.getAttribute('data-icon-label');
    const cls = el.getAttribute('data-icon-class') || '';

    const svg = icon(name, { size, ariaLabel: label, class: cls });
    if (svg) {
      el.innerHTML = svg;
      el.removeAttribute('data-icon');
      el.removeAttribute('data-icon-size');
      el.removeAttribute('data-icon-label');
      el.removeAttribute('data-icon-class');
    }
  });
}

export default { icon, getIconSvg, getIconNames, initIcons, emojiMap };
